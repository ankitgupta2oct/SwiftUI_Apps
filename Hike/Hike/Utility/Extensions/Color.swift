import Foundation
import SwiftUI

extension Color {
    static let theme = ColorTheme()
}

struct ColorTheme {
    let grayLight = Color("ColorGrayLight")
    let grayMedium = Color("ColorGrayMedium")
    let greenDark = Color("ColorGreenDark")
    let greenLight = Color("ColorGreenLight")
    let greenMedium = Color("ColorGreenMedium")
    let indigoMedium = Color("ColorIndigoMedium")
    let samonLight = Color("ColorSamonLight")
}
